/**Macro Historia */
document.getElementById('titulo_historia').addEventListener('click',function(){
    if (document.getElementById('bloque_historia').style.display ==='none'){
        document.getElementById('bloque_historia').style.display ='block'
        document.getElementById('imagen_icono_historia').style.transform='rotate(90deg)'
        document.getElementById('imagen_icono_historia').style.paddingTop= '0.5em'
    }else{
        document.getElementById('bloque_historia').style.display ='none'
        document.getElementById('imagen_icono_historia').style.transform='rotate(0deg)'
        document.getElementById('imagen_icono_historia').style.paddingTop= '.3em'
    };
});
